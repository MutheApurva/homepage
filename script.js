document
  .getElementById("comment-submit-button")
  .addEventListener("click", (event) => {
    let element_comment_input = document.getElementById("comment-input");
    let new_li = document.createElement("li");
    let element_comments_container = document.getElementById(
      "comments-container"
    );
    new_li.innerText = element_comment_input.value;

    new_li.classList.add("custom-font"); // Apply the custom class to the new li element

    element_comments_container.appendChild(new_li);
  });

const navToggle = document.querySelector(".nav-toggle");
const navLinks = document.querySelectorAll(".nav__link");

navToggle.addEventListener("click", () => {
  document.body.classList.toggle("nav-open");
});

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    document.body.classList.remove("nav-open");
  });
});

